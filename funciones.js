function calcularProducto(a, b) {
    return a * b;
}

const resultado = calcularProducto(4, 3);
console.log(resultado); // Imprime 12
function fahrenheitACelsius(fahrenheit) {
    return (5 / 9) * (fahrenheit - 32);
}


const temperaturaCelsius = fahrenheitACelsius(77);
console.log(temperaturaCelsius); 
